const validate = (schema) => (req, res, next) => {
  const { error, value } = schema.validate(req.body, {
    abortEarly: false,
    stripUnknown: true
  });

  if (error) {
    return next({
      status: 400,
      message: error.details.map((detail) => detail.message).join(", ")
    });
  }

  req.body = value;
  return next();
};

module.exports = { validate };